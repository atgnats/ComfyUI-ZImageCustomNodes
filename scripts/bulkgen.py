class GenParams:
    """
    Represents a generation parameter configuration (with multiple optional values)

    This class allows storing multiple values for each parameter to support
    combinatorial generation. For example, if you want to test different
    checkpoints, distinct steps, and different seeds but with the same prompt.

    Example Usage:
        multi_params = GenParams(
            prompt="A cyberpunk city",
            style=["anime", "phone photo"]
            checkpoint=["z_image_turbo-Q5_K_S.gguf", "z_image_turbo_bf16.safetensors"],
            steps=[5,7,9],
            seed=[1]
        )
        # Later logic could generate all images using `self.generate_combinations()`

    Attributes:
        prompt        (str)        : The single prompt text shared across all variations.
        checkpoint    (List[str])  : List of model checkpoint names.
        text_encoder  (List[str])  : List of text encoder names.
        style         (List[str])  : List of artistic styles to test.
        steps         (List[int])  : List of denoising step counts.
        seed          (List[int])  : List of random seeds.
        incalibration (List[float]): List of initial noise calibration values.
        filename      (str)        : Optional filename for the image generated by this parameter set.
    """
    def __init__(self, *,
                 prompt       : str         | None = None,
                 checkpoint   : list[str]   | None = None,
                 text_encoder : list[str]   | None = None,
                 style        : list[str]   | None = None,
                 steps        : list[int]   | None = None,
                 seed         : list[int]   | None = None,
                 incalibration: list[float] | None = None,
                 filename     : str         | None = None,
    ) -> None:
        self.prompt        = prompt        if type(prompt)        == str  else DEFAULT_PROMPT
        self.checkpoint    = checkpoint    if type(checkpoint)    == list else [DEFAULT_CHECKPOINT]
        self.text_encoder  = text_encoder  if type(text_encoder)  == list else [DEFAULT_TEXT_ENCODER]
        self.style         = style         if type(style)         == list else [DEFAULT_STYLE]
        self.steps         = steps         if type(steps)         == list else [int(DEFAULT_STEPS)]
        self.seed          = seed          if type(seed)          == list else [int(DEFAULT_SEED)]
        self.incalibration = incalibration if type(incalibration) == list else [float(DEFAULT_INCALIBRATION)]
        self.filename      = filename      if type(filename)      == str  else ""


    def set_param(self, param_and_values: str):
        """
        Updates a generation parameter based on a string with format "param = value"

        This method allows modifying parameters like checkpoint, style, steps, etc.,
        using a key-value string format. Param names can be prefixed with "$".

        Args:
            param_and_values (str): The parameter name and values in the format "param=value"
                                    or "param=value1,value2,...".
                                    Example: "ckpt=zimage.safetensors" or "$steps=5,7,9".
        Raises:
            ValueError: If the parameter name is not recognized (e.g., unknown param).
            ValueError: If value conversion fails (handled by to_int/to_float).

        Supported Parameters:
            - "ckpt" : Updates self.checkpoint.
            - "style": Updates self.style.
            - "steps": Updates self.steps.
            - "seed" : Updates self.seed .
            - "inc", "incalibration", "initial_noise_calibration": Updates self.incalibration.
        """
        param, _, values = param_and_values.partition("=")
        param  = param.strip().lower()
        if param.startswith('$'): param = param[1:]

        # builds the list of values
        values = [v.strip() for v in values.split(",")]

        if param == "ckpt":
            self.checkpoint = values
        elif param == "style":
            self.style = values
        elif param == "steps":
            self.steps = to_int(values)
        elif param == "seed":
            self.seed  = to_int(values)
        elif param == "inc" or param == "incalibration" or param == "initial_noise_calibration":
            self.incalibration = to_float(values)
        else:
            raise ValueError(f"Unknown generation parameter: {param}")


    def generate_combinations(self, name_prefix="bulk") -> list["GenParams"]:
        """
        Generates a list of GenParams objects representing every possible
        combination of the multi-valued parameters defined in this instance.

        Returns:
            List[GenParams]: A list containing unique GenParams configurations.

        Example:
            params_list = self.generate_combinations()
        """
        from itertools import product
        result_list: list[GenParams] = []

        # define the options to iterate over
        options = [ self.checkpoint,
                    self.style,
                    self.steps,
                    self.seed,
                    self.incalibration ]

        # create a new GenParams instance for each combination
        for combo in list(product(*options)):
            filename = name_prefix
            if len(self.steps)>1: filename += f"_steps{combo[2]}"
            if len(self.seed )>1: filename += f"_seed{combo[3]}"
            genparams = GenParams(
                prompt        = self.prompt,
                filename      = filename,
                checkpoint    = [ combo[0] ],
                style         = [ combo[1] ],
                steps         = [ combo[2] ],
                seed          = [ combo[3] ],
                incalibration = [ combo[4] ],
            )
            result_list.append(genparams)

        return result_list


    def copy(self) -> 'GenParams':
        """Returns a deep copy with all attributes duplicated independently.
        Example:
            original = GenParams(prompt="A cyberpunk city", steps=[5, 7, 9])
            copy_instance = original.copy()
            # Modifying copy_instance.steps will not affect original
            original.steps.append(10)
            assert copy_instance.steps == [5, 7, 9]  # True
        """
        return deepcopy(self)


    def __str__(self) -> str:
        """Returns a readable string representation of the multi-value configuration."""
        return f"GenParams(checkpoint={self.checkpoint}, style={self.style}, "         \
               f"steps={self.steps}, seed={self.seed}, incalibration={self.incalibration}, "\
               f"prompt=\"{self.prompt}\")"


