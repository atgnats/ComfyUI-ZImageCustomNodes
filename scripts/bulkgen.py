class ImageBuilder:
    """
    Generates images through the ComfyUI API.
    """
    def __init__(self, comfyapi: ComfyAPI) -> None:
        self._confyapi        = comfyapi
        self._last_checkpoint = ""


    def __call__(self, genparams: GenParams) -> bytes:
        """Convenience alias for `build_images()`"""
        return self.build_image(genparams)


    def build_image(self, genparams: GenParams) -> bytes:
        """
        Generates images according to the provided generation parameters.

        Args:
            genparams: Object containing generation parameters (style, prompt, seed, etc.).
            comfyapi : The API client instance for interacting with ComfyUI.
        Returns:
            List of generated images (raw bytes).
        """
        # notify user when the checkpoint changes
        if  self._last_checkpoint != genparams.checkpoint[0]:
            self._last_checkpoint  = genparams.checkpoint[0]
            info(f"Loading model \"{genparams.checkpoint[0]}\", this may take a while...")

        workflow     = self.build_workflow(genparams)
        api_workflow = self.build_api_workflow(genparams)
        return self._confyapi.generate_and_wait(api_workflow)[0]


    def build_workflow(self, genparams: GenParams, custom_styles: str = "") -> dict:
        """
        Builds a ComfyUI workflow by loading a template JSON and injecting parameters.

        Loads the "bulkgen.json" workflow file, iterates through nodes, and
        updates widget values based oon the provided generation parameters.

        Args:
            genparams    : A `GenParams` object containing generation parameters (style, prompt, seed, etc.).
            custom_styles: Optional custom style templates.
        Returns:
            The modified workflow dictionary with updated generation parameters.
        """
        workflow_file = SCRIPT_DIR + "/bulkgen.json"
        with open(workflow_file, 'r') as f:
            workflow = json.load(f)

        # get the workflow's list of nodes
        nodes = workflow.get("nodes")
        if not nodes: return {}

        for node in nodes:
            ntype : str = node.get("type","")
            title : str = node.get("title","")
            title_lower = title.lower()

            # README
            if ntype == "Note" and title_lower == "readme":
                node["widgets_values"] = [ README_NOTE ]

            # CUSTOM STYLES
            if ntype == "PrimitiveStringMultiline" and "styles" in title_lower:
                node["widgets_values"] = [ custom_styles ]

            # Style + Prompt
            if "StylePrompt" in ntype and node_has_input(node,"clip"):
                number_of_widgets = len(node.get("widgets_values",[]))
                if number_of_widgets != 4:
                    warning(f"Node \"{title}\" has {number_of_widgets} widgets, expected 4")
                node["widgets_values"] = [
                    f'"{genparams.style[0]}"', None, None, genparams.prompt ]

            # Seed + Steps + InCalibration
            if "ZSamplerTurbo" in ntype and node_has_input(node,"model"):
                number_of_widgets = len(node.get("widgets_values",[]))
                if number_of_widgets != 7:
                    warning(f"Node \"{title}\" has {number_of_widgets} widgets, expected 7")
                node["widgets_values"] = [
                    genparams.seed[0], "fixed", genparams.steps[0], DEFAULT_DENOISE, None,
                    fix_percent(genparams.incalibration[0], use_off=True),
                    DEFAULT_LOWRES_BIAS ]

            # Checkpoint
            if ntype == "UnetLoaderGGUF":
                node["widgets_values"] = [
                    genparams.checkpoint[0] ]

            # TextEncoder
            if ntype == "CLIPLoaderGGUF":
                node["widgets_values"] = [
                    genparams.text_encoder[0], "lumina2" ]

        return workflow


    def build_api_workflow(self, genparams: GenParams, custom_styles: str = "") -> dict:
        """
        Builds a ComfyUI API workflow by loading a template JSON and updating inputs.

        Loads the "bulkgen.api.json" workflow file, iterates through nodes, and
        updates the input values based on the provided generation parameters.

        Args:
            genparams    : A `GenParams` object containing generation parameters (style, prompt, seed, etc.).
            custom_styles: Optional custom style templates.
        Returns:
            The modified API workflow dictionary, ready to be sent to "/prompt"
            via the ComfyUI REST API.
        """
        api_workflow_file = SCRIPT_DIR + "/bulkgen.api.json"
        with open(api_workflow_file, 'r') as f:
            api_workflow = json.load(f)

        for _, node in api_workflow.items():
            ntype  : str  = node.get("class_type","")
            inputs : dict = node.get("inputs",{})

            # Style + Prompt
            if ntype.startswith("StylePrompt") and ntype.endswith("ZImagePowerNodes"):
                inputs["style"] = genparams.style[0]
                inputs["text" ] = genparams.prompt

            # # Seed + Steps + InCalibration
            if ntype.startswith("ZSamplerTurbo") and ntype.endswith("ZImagePowerNodes"):
                inputs["seed"       ] = genparams.seed[0]
                inputs["steps"      ] = genparams.steps[0]
                inputs["initial_noise_calibration"] = fix_percent(genparams.incalibration[0], use_off=True)
                inputs["denoise"    ] = DEFAULT_DENOISE
                inputs["lowres_bias"] = DEFAULT_LOWRES_BIAS

            # Checkpoint
            if ntype == "UnetLoaderGGUF":
                inputs["unet_name"] = genparams.checkpoint[0]

            # TextEncoder
            if ntype == "CLIPLoaderGGUF":
                inputs["clip_name"] = genparams.text_encoder[0]

        return api_workflow



