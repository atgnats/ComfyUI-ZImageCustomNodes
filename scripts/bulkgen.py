def main(args=None, parent_script=None):
    """
    Main entry point for the script.
    Args:
        args          (optional): List of arguments to parse. Default is None, which will use the command line arguments.
        parent_script (optional): The name of the calling script if any. Used for customizing help output.
    """
    prog = None
    if parent_script:
        prog = parent_script + " " + os.path.basename(__file__).split('.')[0]

    # set up argument parser for the script
    parser = argparse.ArgumentParser(
        prog            = prog,
        description     = "Create a batch of images based on a configuration file",
        formatter_class = argparse.RawTextHelpFormatter,
        epilog          = """Environment Variables:
  BULKGEN_COMFY_HOST / COMFY_HOST = Default comfyui host address.
  BULKGEN_COMFY_PORT / COMFY_PORT = Default comfyui port number.
  """)
    parser.add_argument('--no-color'  , action='store_true'      , help="Disable colored output.")
    parser.add_argument('--host'      , type=str                 , help="ComfyUI host address.")
    parser.add_argument('--port'      , type=str                 , help="ComfyUI port.")
    parser.add_argument('config_files', nargs='+', metavar='FILE', help="One or more configuration files to generate images from.")
    args = parser.parse_args(args=args)

    # if the user requested to disable colors, call disable_colors()
    if args.no_color:
        disable_colors()

    # try to connect to ComfyUI,
    # the host and port are determined by the --host and --port flags
    # (if not specified, the values from environment variables are used)
    comfy_host = args.host or DEFAULT_HOST
    comfy_port = args.port or DEFAULT_PORT
    comfyapi = ComfyAPI(comfy_host, comfy_port)
    if not comfyapi.check_connection():
        fatal_error(f"Could not connect to ComfyUI at \"{comfy_host}:{comfy_port}\"",
                    "Use the --host and --port flags to specify the host and port.",
                    "Alternatively, you can set the BULKGEN_COMFY_HOST and BULKGEN_COMFY_PORT environment variables.")

    image_builder = ImageBuilder(comfyapi)

    # loop through the configuration files and generate images
    for config_file in args.config_files:
        config = Config.from_file(config_file)
        all_genparams = config.all_genparams
        custom_styles = config.custom_styles

        for prompt_index, genparams in enumerate(all_genparams):
            filename_prefix = f"{FILE_PREFIX}_p{prompt_index:02}"
            genparams_list = genparams.generate_combinations(name_prefix=filename_prefix)
            for params in genparams_list:
                image_bytes = image_builder(params)
                with open(f"{params.filename}.png", "wb") as f:
                    f.write(image_bytes)




